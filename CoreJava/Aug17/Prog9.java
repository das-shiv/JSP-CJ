class Prog9
{
    public static void main(String[] args) {
        int a =30;
        System.out.println(a);
        a+=30;// a=30+30=60
        System.out.println(a);
        a-=10;// a= 60-10 =50
        System.out.println(a);
        a*=5;//a=50*5=250
        System.out.println(a);
        a/=10;// a=250/10=25
        System.out.println(a);
        a%=20;//a=25%20=5
        System.out.println(a);

    }
}
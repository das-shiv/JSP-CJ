class Literalss
{
	public static void main(String[] args)
	{
	System.out.println(1);//integer
	System.out.println(123.456);//float
	System.out.println(-123);//negative integer
    System.out.println(-123.647);//negative floating
	System.out.println('q');//char
	System.out.println("Hello World");//string
    System.out.println(true);//boolean
	}
}